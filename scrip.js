function showModal() {
    var element = document.getElementById("modal");
    element.classList.add("show-modal");
}

function closeModal() {
    var element = document.getElementById("modal");
    element.classList.remove("show-modal");
}



function abreModal() {
    var element = document.getElementById("modals");
    element.classList.add("show-modal");
}

function fechaModal() {
    var element = document.getElementById("modals");
    element.classList.remove("show-modal");
}



